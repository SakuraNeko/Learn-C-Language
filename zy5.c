#include<stdio.h>
int main(){
	double f;
		while(scanf("%lf",&f) != EOF){
			if(f<=100000){f=0.1*f;} 
			if(100000<f&&f<=200000){f=10000+0.075*(f-100000);} 
			if(200000<f&&f<=400000){f=17500+0.05*(f-200000);} 
			if(400000<f&&f<=600000){f=27500+0.03*(f-400000);}
			if(600000<f&&f<=1000000){f=33500+0.015*(f-600000);}  
			if(1000000<f){f=39500+0.01*(f-1000000);}
		printf("%.2lf\n",f);
		}
}
